        for 副产品序号 in range(len(精英材料编号列表[2])):
            加工T4获得T3精英材料矩阵[副产品序号, 配方序号] += T3精英材料权重[副产品序号] / T3精英材料总权重 * 精英材料副产品产出概率[1]
